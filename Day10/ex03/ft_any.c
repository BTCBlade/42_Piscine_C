int		ft_any(char **tab, int (*f)(char*))
{
	int i;
	int result;

	result = 0;
	i = 0;
	while (tab[i])
	{
		result = f(tab[i]);
		if (result)
		{
			return (result);
		}
		i++;
	}
	return (result);
}
